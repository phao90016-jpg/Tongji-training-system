-- 创建视图：培训完成率KPI
CREATE OR REPLACE VIEW V_DEPT_KPI AS
SELECT 
    D.DEPT_NAME,
    COUNT(DISTINCT E.EMP_ID) AS TOTAL_EMP,
    COUNT(DISTINCT CASE WHEN TR.STATUS = '已完成' THEN TR.EMP_ID END) AS COMPLETED_EMP,
    ROUND(COUNT(DISTINCT CASE WHEN TR.STATUS = '已完成' THEN TR.EMP_ID END) * 100.0 / COUNT(DISTINCT E.EMP_ID), 2) AS COMPLETION_RATE
FROM DEPARTMENTS_TRAINING D
LEFT JOIN EMPLOYEES E ON D.DEPT_NAME = E.DEPT_NAME
LEFT JOIN TRAINING_REGISTRATIONS TR ON E.EMP_ID = TR.EMP_ID
GROUP BY D.DEPT_NAME;

-- 创建视图：预算使用情况
CREATE OR REPLACE VIEW V_BUDGET_USAGE AS
SELECT 
    DEPT_NAME,
    ANNUAL_BUDGET,
    USED_BUDGET,
    REMAIN_BUDGET,
    ROUND(USED_BUDGET * 100.0 / ANNUAL_BUDGET, 2) AS USAGE_RATE
FROM DEPARTMENTS_TRAINING;

